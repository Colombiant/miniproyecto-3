package edu.univalle.cincuentazo;

/**
 * Plain Java launcher used by IDEs that do not configure the JavaFX module path automatically.
 */
public final class Launcher {

    private Launcher() {
        // Utility class.
    }

    /**
     * Starts the JavaFX application through a non-JavaFX main class.
     *
     * @param args command line arguments
     */
    public static void main(String[] args) {
        CincuentazoApplication.main(args);
    }
}
